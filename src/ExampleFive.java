public class ExampleFive {
    public static void main(String[] args) {
        int day = 8;
        switch (day) {
            case 6:
                System.out.println("Today is Saturday");
                break;
            case 7:
                System.out.println("Today is Sunday");
                break;
            default:
                System.out.println("Looking forward for the weekend");
        }
    }
}
